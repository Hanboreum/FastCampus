package fc.java.part2;

public class AssignOperTest {
    public static void main(String[] args) {
        int a= 1;
        a++;
        a--;
        --a;
        ++a;
        a+=1;
        System.out.println("a = " + a);

        int b=18;
        b--;
        ++b;
        --b;
        b+=4;
        System.out.println("b = " + b);

    }
}
